// 날씨 타입 결정 함수
export const getWeatherType = (temperature) => {
  if (temperature >= 35) return 'extremeHeat';
  if (temperature >= 30) return 'veryHot';
  if (temperature >= 27) return 'hot';
  if (temperature >= 23) return 'warm';
  if (temperature >= 17) return 'mild';
  if (temperature >= 12) return 'cool';
  if (temperature >= 5) return 'cold';
  if (temperature >= 0) return 'veryCold';
  return 'freezing';
};

